import { Link } from '@inertiajs/react';

export default function ResponsiveNavLink({
    active = false,
    className = '',
    children,
    ...props
}) {
    return (
        <Link
            {...props}
            className={`flex w-full items-start border-l-4 py-2 pe-4 ps-3 ${
                active
                    ? 'border-white bg-orange-500 text-white focus:border-white focus:bg-white focus:text-white'
                    : 'border-transparent text-white hover:border-white hover:bg-white hover:text-black focus:border-white focus:bg-white focus:text-white'
            } text-base font-medium transition duration-150 ease-in-out focus:outline-none ${className}`}
        >
            {children}
        </Link>
    );
}
